    void VirtualizationManager::run_virt_menu() {
        while (true) {
            std::string menu = cfg_.tui_bin +
                               " --title \"" + _("virt.title") + "\""
                               " --menu \"" + _("virt.menu_prompt") + "\" 0 0 0 "
                               "\"1\" \"" + _("virt.qemu") + "\" "
                               "\"2\" \"" + _("virt.download_iso") + "\" "
                               "\"3\" \"" + _("virt.docker") + "\" "
                               "\"4\" \"" + _("virt.virtualbox") + "\" "
                               "\"5\" \"" + _("virt.wine") + "\" "
                               "\"6\" \"" + _("virt.virt_manager") + "\" "
                               "\"0\" \"" + _("menu.tui.back") + "\"";

            std::string choice = Executor::tui_select(menu);
            if (choice == "0" || choice.empty()) break;

            if (choice == "1") {
                qemu_tui_wizard();
            } else if (choice == "2") {
                run_iso_menu();
            } else if (choice == "3") {
                // Docker 管理委托给 DockerManager
                // 这里输出提示引导到 Docker 菜单
                Logger::info("Docker 管理请在主菜单选择对应选项");
                Logger::info("或通过 CLI: tmoes docker");
            } else if (choice == "4") {
                install_virtualbox();
            } else if (choice == "5") {
                run_wine_menu();
            } else if (choice == "6") {
                install_virt_manager();
            }
            Logger::press_enter();
        }
    }

