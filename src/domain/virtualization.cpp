    void VirtualizationManager::ensure_vm_dir() const {
        fs::create_directories(vm_store_dir());
    }

    bool VirtualizationManager::qemu_create_disk(std::string_view name,
                                                 int64_t size_gb,
                                                 std::string_view format) {
        ensure_vm_dir();
        fs::path disk_path = vm_store_dir() / (std::string(name) + "." + std::string(format));

        std::ostringstream cmd;
        cmd << "qemu-img create -f " << format
                << " \"" << disk_path.string() << "\" " << size_gb << "G";

        auto result = Executor::shell(cmd.str());
        if (result.ok()) {
            Logger::ok("磁盘镜像已创建: " + disk_path.string() + " (" +
                       std::to_string(size_gb) + " GB)");
        }
        return result.ok();
    }

    bool VirtualizationManager::qemu_compress_disk(std::string_view disk_path) {
        Logger::step("压缩 QCOW2 磁盘: " + std::string(disk_path));

        fs::path original(disk_path);
        fs::path compressed = original.parent_path() /
                              (original.stem().string() + "_compressed.qcow2");

        std::string cmd = "qemu-img convert -c -O qcow2 \"" +
                          std::string(disk_path) + "\" \"" +
                          compressed.string() + "\"";
        auto result = Executor::passthrough(cmd);
        if (result.ok()) {
            auto orig_size = fs::file_size(original) / (1024 * 1024);
            auto comp_size = fs::file_size(compressed) / (1024 * 1024);
            Logger::ok("磁盘已压缩: " + std::to_string(orig_size) + " MB → " +
                       std::to_string(comp_size) + " MB");
            Logger::info("压缩后文件: " + compressed.string());
        }
        return result.ok();
    }

    bool VirtualizationManager::qemu_create(std::string_view name,
                                            int64_t disk_size_gb,
                                            int ram_mb, int cpu_cores) {
        Logger::step("创建 QEMU 虚拟机: " + std::string(name));

        ensure_vm_dir();

        // 创建磁盘
        if (!qemu_create_disk(name, disk_size_gb)) {
            return false;
        }

        // 生成启动脚本
        fs::path script_path = vm_store_dir() / (std::string(name) + "_start.sh");
        std::ostringstream script;

        script << "#!/bin/bash\n"
                << "# Auto-generated by tmoes — " << name << "\n\n"
                << "DISK=\"" << vm_store_dir().string() << "/" << name << ".qcow2\"\n"
                << "qemu-system-x86_64 \\\n"
                << "  -machine q35,accel=kvm \\\n"
                << "  -cpu host \\\n"
                << "  -smp sockets=1,cores=" << cpu_cores << ",threads=1 \\\n"
                << "  -m " << ram_mb << " \\\n"
                << "  -drive file=\"$DISK\",if=virtio,format=qcow2 \\\n"
                << "  -vnc :1 \\\n"
                << "  -device virtio-net-pci,netdev=net0 \\\n"
                << "  -netdev user,id=net0\n";

        std::ofstream ofs(script_path);
        if (!ofs) return false;
        ofs << script.str();
        ofs.close();

        // 设置可执行权限
        Executor::shell("chmod +x \"" + script_path.string() + "\"");

        Logger::ok("虚拟机已创建: " + std::string(name));
        Logger::info("启动脚本: " + script_path.string());
        Logger::info("磁盘镜像: " + (vm_store_dir() / (std::string(name) + ".qcow2")).string());

        return true;
    }

    bool VirtualizationManager::qemu_start(std::string_view name) {
        Logger::step("启动 QEMU 虚拟机: " + std::string(name));

        fs::path script = vm_store_dir() / (std::string(name) + "_start.sh");
        if (fs::exists(script)) {
            std::string cmd = "bash \"" + script.string() + "\" &";
            Executor::passthrough(cmd);
            Logger::ok("虚拟机正在启动 (后台)...");
            return true;
        }

        // 尝试直接 QEMU 启动
        fs::path disk = vm_store_dir() / (std::string(name) + ".qcow2");
        if (fs::exists(disk)) {
            std::string cmd = "qemu-system-x86_64 -machine q35,accel=kvm "
                              "-cpu host -m 2048 "
                              "-drive file=\"" + disk.string() + "\",if=virtio "
                              "-vnc :1 -netdev user,id=net0 "
                              "-device virtio-net-pci,netdev=net0 &";
            Executor::passthrough(cmd);
            Logger::ok("虚拟机正在启动 (后台)...");
            Logger::info("VNC 地址: 127.0.0.1:5901");
            return true;
        }

        Logger::error("未找到虚拟机 " + std::string(name) + "，请先使用 qemu_create 创建");
        return false;
    }

    bool VirtualizationManager::qemu_stop(std::string_view name) {
        Logger::step("停止 QEMU 虚拟机...");

        // 通过 qemu monitor 或 kill 发送关机信号
        Executor::shell("pkill -f \"qemu-system.*" + std::string(name) + "\" 2>/dev/null");
        Logger::ok("已发送停止信号");
        return true;
    }

    std::vector<std::string> VirtualizationManager::qemu_list() const {
        std::vector<std::string> result;
        auto exec = Executor::shell("ps aux | grep qemu-system | grep -v grep");
        if (exec.ok()) {
            std::istringstream iss(exec.stdout_data);
            std::string line;
            while (std::getline(iss, line)) {
                if (!line.empty()) result.push_back(line);
            }
        }
        return result;
    }

    bool VirtualizationManager::qemu_tui_wizard() {
        while (true) {
            auto running = qemu_list();

            std::string menu = cfg_.tui_bin +
                               " --title \"" + _("virt.qemu_title") + "\""
                               " --menu \"" + _("virt.qemu_prompt") + " " +
                               std::to_string(running.size()) + "):\" 0 0 0 "
                               "\"1\" \"" + _("virt.qemu_create") + "\" "
                               "\"2\" \"" + _("virt.qemu_start") + "\" "
                               "\"3\" \"" + _("virt.qemu_stop") + "\" "
                               "\"4\" \"" + _("virt.qemu_list") + "\" "
                               "\"5\" \"" + _("virt.qemu_compress") + "\" "
                               "\"0\" \"" + _("menu.tui.back") + "\"";

            std::string choice = Executor::tui_select(menu);
            if (choice == "0" || choice.empty()) break;

            if (choice == "1") {
                // 交互式创建向导
                std::string name_cmd = cfg_.tui_bin +
                                       " --title \"" + _("virt.qemu_name_title") + "\" --inputbox \"" + _("virt.qemu_name_input") + "\" 0 0 \"my-vm\"";
                auto result = Executor::passthrough("echo \"$(" + name_cmd + " 2>&1 1>/dev/tty)\"");
                std::string name = result.stdout_data;
                name.erase(std::remove(name.begin(), name.end(), '\n'), name.end());
                if (name.empty()) continue;

                std::string disk_cmd = cfg_.tui_bin +
                                       " --title \"" + _("virt.qemu_disk_title") + "\" --inputbox \"" + _("virt.qemu_disk_input") + "\" 0 0 \"10\"";
                result = Executor::passthrough("echo \"$(" + disk_cmd + " 2>&1 1>/dev/tty)\"");
                int64_t disk_gb = 10;
                try { disk_gb = std::stoll(result.stdout_data); } catch (...) {
                }

                std::string ram_cmd = cfg_.tui_bin +
                                      " --title \"" + _("virt.qemu_ram_title") + "\" --inputbox \"" + _("virt.qemu_ram_input") + "\" 0 0 \"2048\"";
                result = Executor::passthrough("echo \"$(" + ram_cmd + " 2>&1 1>/dev/tty)\"");
                int ram = 2048;
                try { ram = std::stoi(result.stdout_data); } catch (...) {
                }

                std::string cpu_cmd = cfg_.tui_bin +
                                      " --title \"" + _("virt.qemu_cpu_title") + "\" --inputbox \"" + _("virt.qemu_cpu_input") + "\" 0 0 \"2\"";
                result = Executor::passthrough("echo \"$(" + cpu_cmd + " 2>&1 1>/dev/tty)\"");
                int cpu = 2;
                try { cpu = std::stoi(result.stdout_data); } catch (...) {
                }

                qemu_create(name, disk_gb, ram, cpu);
            } else if (choice == "2") {
                // 列出已有虚拟机让用户选择
                std::vector<std::string> vms;
                for (const auto &entry: fs::directory_iterator(vm_store_dir())) {
                    auto name = entry.path().stem().string();
                    if (name.find("_start") == std::string::npos &&
                        entry.path().extension() == ".qcow2") {
                        vms.push_back(name);
                    }
                }
                if (vms.empty()) {
                    Logger::warn("未找到已创建的虚拟机");
                    continue;
                }
                std::string sub_menu = cfg_.tui_bin +
                                       " --title \"" + _("virt.qemu_select_title") + "\" --menu \"" + _("virt.qemu_select_prompt") + "\" 0 0 0 ";
                for (size_t i = 0; i < vms.size(); ++i) {
                    sub_menu += "\"" + std::to_string(i + 1) + "\" \"" + vms[i] + "\" ";
                }
                sub_menu += "\"0\" \"" + _("menu.tui.back") + "\"";
                std::string sub = Executor::tui_select(sub_menu);
                if (sub != "0" && !sub.empty()) {
                    int idx = std::stoi(sub) - 1;
                    if (idx >= 0 && idx < static_cast<int>(vms.size())) {
                        qemu_start(vms[idx]);
                    }
                }
            } else if (choice == "3") {
                qemu_stop("all");
            } else if (choice == "4") {
                auto list = qemu_list();
                if (list.empty()) {
                    Logger::info("当前无运行中的 QEMU 虚拟机");
                } else {
                    Logger::info("运行中的虚拟机:");
                    for (const auto &l: list) {
                        Logger::info("  " + l);
                    }
                }
            } else if (choice == "5") {
                std::string path_cmd = cfg_.tui_bin +
                                       " --title \"" + _("virt.qemu_disk_path_title") + "\" --inputbox \"" + _("virt.qemu_disk_path_input") + "\" 0 0";
                auto result = Executor::passthrough("echo \"$(" + path_cmd + " 2>&1 1>/dev/tty)\"");
                std::string path = result.stdout_data;
                path.erase(std::remove(path.begin(), path.end(), '\n'), path.end());
                if (!path.empty() && fs::exists(path)) {
                    qemu_compress_disk(path);
                }
            }
            Logger::press_enter();
        }
        return true;
    }

