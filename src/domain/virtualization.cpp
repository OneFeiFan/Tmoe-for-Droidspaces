    bool VirtualizationManager::download_iso(std::string_view os_name) {
        auto isos = available_isos();
        const IsoInfo *target = nullptr;

        for (const auto &iso: isos) {
            if (iso.name == os_name) {
                target = &iso;
                break;
            }
        }

        if (!target) {
            Logger::error("不支持的 ISO: " + std::string(os_name));
            return false;
        }

        ensure_vm_dir();
        fs::path iso_path = vm_store_dir() / (std::string(os_name) + ".iso");

        Logger::step("下载 " + std::string(os_name) + " ISO...");
        Logger::info("来源: " + target->url);
        Logger::info("保存至: " + iso_path.string());
        Logger::info("(下载大型 ISO 可能需要较长时间)");

        // 使用 wget 或 curl 下载
        std::string dl_cmd = "wget -O \"" + iso_path.string() +
                             "\" \"" + target->url + "\" 2>&1";
        // 如果 wget 不可用，回退到 curl
        if (!Executor::has("wget")) {
            dl_cmd = "curl -L -o \"" + iso_path.string() +
                     "\" \"" + target->url + "\"";
        }

        auto result = Executor::passthrough(dl_cmd);
        if (result.ok() && fs::exists(iso_path)) {
            auto size_mb = fs::file_size(iso_path) / (1024 * 1024);
            Logger::ok("ISO 下载完成: " + iso_path.string() +
                       " (" + std::to_string(size_mb) + " MB)");
            return true;
        }

        Logger::error("ISO 下载失败");
        return false;
    }

    bool VirtualizationManager::download_disk_image(std::string_view template_name) {
        Logger::step("下载预构建磁盘镜像: " + std::string(template_name));
        Logger::info("预构建镜像下载目前需要手动从上游获取");
        Logger::info("推荐使用 alpine-docker 模板快速开始");

        if (template_name == "alpine-docker") {
            ensure_vm_dir();
            // 创建带 Docker 的 Alpine 磁盘
            qemu_create_disk("alpine-docker", 5);

            Logger::info("请使用 QEMU 启动后手动安装 Docker:");
            Logger::info("  qemu-system-x86_64 -cdrom alpine.iso -hda alpine-docker.qcow2");
            Logger::info("然后运行: apk add docker && rc-update add docker boot");
            return true;
        }

        Logger::warn("预构建模板 " + std::string(template_name) + " 暂未实现自动下载");
        return false;
    }

    void VirtualizationManager::run_iso_menu() {
        while (true) {
            auto isos = available_isos();

            std::string menu = cfg_.tui_bin +
                               " --title \"" + _("virt.iso_title") + "\""
                               " --menu \"" + _("virt.iso_prompt") + "\" 0 0 0 ";

            for (size_t i = 0; i < isos.size(); ++i) {
                menu += "\"" + std::to_string(i + 1) + "\" \""
                        + isos[i].name + " — " + isos[i].description + "\" ";
            }

            // 预构建模板
            auto templates = available_disk_templates();
            for (size_t i = 0; i < templates.size(); ++i) {
                menu += "\"" + std::to_string(isos.size() + i + 1) + "\" \""
                        + "[模板] " + templates[i].second + "\" ";
            }

            menu += "\"0\" \"" + _("menu.tui.back") + "\"";

            std::string choice = Executor::tui_select(menu);
            if (choice == "0" || choice.empty()) break;

            int idx = std::stoi(choice) - 1;
            if (idx >= 0 && idx < static_cast<int>(isos.size())) {
                download_iso(isos[idx].name);
            } else {
                int tidx = idx - isos.size();
                if (tidx >= 0 && tidx < static_cast<int>(templates.size())) {
                    download_disk_image(templates[tidx].first);
                }
            }
            Logger::press_enter();
        }
    }

