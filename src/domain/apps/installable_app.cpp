bool InstallableApp::confirm_install() {
    // 子类可覆盖以实现特定确认逻辑
    // 默认：始终确认（静默安装）
    return true;
}

bool InstallableApp::confirm_remove() {
    // 使用 dialog::yesno 替代硬编码 whiptail
    return ui::dialog::yesno(cfg_, name(), _f("app.confirm_remove", name()), "", "", 8, 50) == 0;
}

