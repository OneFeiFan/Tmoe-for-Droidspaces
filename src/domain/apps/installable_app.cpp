bool InstallableApp::install(DistroFamily family) {
    Logger::step(name());

    // 1. 安装前钩子（PPA、GPG key 等）
    if (!pre_install(family)) {
        Logger::warn(_("app.install_cancelled") + ": " + name());
        return false;
    }

    // 2. 解析包名并安装
    auto pkgs = packages().get(family);
    if (pkgs.empty()) {
        Logger::error(_f("app.no_packages_for_distro", name()));
        return false;
    }

    if (!PackageManager::install(pkgs, family)) {
        return false;
    }

    // 3. 安装后钩子（配置、wrapper、desktop 等）
    post_install(family);

    // 4. 自动创建 no-sandbox wrapper（如果子类声明需要）
    if (needs_sandbox_wrapper()) {
        create_sandbox_wrapper(name(), bin_name());
    }

    return true;
}

bool InstallableApp::remove(DistroFamily family) {
    // 1. 用户确认
    if (!confirm_remove()) {
        return false;
    }

    // 2. 卸载前钩子
    if (!pre_remove(family)) {
        return false;
    }

    // 3. 执行卸载
    auto pkgs = packages().get(family);
    if (pkgs.empty()) {
        // 尝试用 common 回退
        pkgs = DistroPkgNames::split_pkgs(packages().common);
    }
    PackageManager::remove(pkgs, family);

    // 4. 卸载后清理
    post_remove(family);

    return true;
}

// ═══════════════════════════════════════════════════════════════
